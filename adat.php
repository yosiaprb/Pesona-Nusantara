        <h1>Upacara Adat di Indonesia Beserta Tujuannya yang Wajib Kamu Ketahui</h1>
    </div>
    <div class="container" id="image_container">
        <img src="img/banner_adat.jpg" alt="Adat" class="centered_image">
    </div>
    <div class="container" style="padding-top: 2rem;">
        <p>Indonesia memiliki beraneka ragam pakaian adat, tradisi, hingga upacara adat unik yang banyak menarik para
            wisatawan domestik maupun mancanegara untuk menyaksikannya.</p>
        <p>Upacara adat menjadi kekayaan budaya yang diadakan secara turun-temurun sesuai dengan kepercayaan masyarakat
            setempat. Tradisi upacara adat atau ritual sakral ini terdapat di daerah-daerah Indonesia dari Sabang hingga
            Merauke.</p>
        <p>Berikut ini beberapa upacara adat di Indonesia yang menarik beserta tujuan diselenggarakannya:</p>
    </div>
    <div class="container">
        <h3>1. Upacara Adat Ngaben di Bali</h3>
    </div>
    <div class="container">
        <p>Upacara ini merupakan upacara yang terkenal di Bali. Menurut Buku Modul PPKn "Bersama Meskipun Beragam"
            Kemendikbud 2020, Upacara kremasi atau pembakaran jenazah di Bali yang dipercaya oleh masyarakat Hindu
            sebagai ritual untuk menyempurnakan jenazah kembali ke Sang Pencipta.</p>
        <p>Upacara ini menjadi simbol untuk menyucikan roh orang yang telah meninggal. Tujuannya untuk mengirimkan
            jenazah ek kehidupan mendatang.</p>
        <p>Upacara Ngaben terbagi menjadi tiga jenis yaitu, Ngaben sawa Wedana, Ngaben Asti Wedana, dan Swasta.</p>
    </div>
    <div class="container">
        <h3>2. Upacara Rambu Solo di Toraja</h3>
    </div>
    <div class="container">
        <p>Upacara berikutnya yakni upacara Rambu Solo di Toraja. Menurut Buku Modul PPKn "Bersama Meskipun Beragam"
            Kemendikbud 2020, Upacara ini mirip dengan Ngaben di Bali.</p>
        <p>Ritual ini dimaksudkan untuk mengantarkan arwah orang yang sudah meninggal menuju alam roh, dan berkumpul
            bersama para leluhur. Seseorang yang telah meninggal namun belum menjalani upacara tradisi Rambu Solo masih
            dianggap sebagai orang sakit dan belum dimakamkan.</p>
    </div>
    <div class="container">
        <h3>3. Upacara Bakar Batu di Papua</h3>
    </div>
    <div class="container">
        <p>Mengutip dari Buku PPKn "Indonesiaku Unik" Kemendikbud 2018, tradisi bakar batu merupakan tradisi atas
            ungkapan rasa syukur atas berkat yang melimpah seperti kelahiran, perkawinan adat, atau penobatan kepala
            suku.</p>
        <p>Tradisi ini menjadi ajang berkumpul warga dan menjadi solidaritas kebersamaan masyarakat Papua. Tidak hanya
            itu pesta tersebut menjadi ungkapan saling memaafkan antar warga dan perdamaian antar suku.</p>
        <p>Tradisi bakar batu disebut juga dengan Barapen, Gapii atau Kit Oba Isogoa (Wamena). Dalam tradisi ini makanan
            yang akan disajikan dalam pesta dibakar di atas batu. Pada umumnya tempat untuk melakukan tradisi batu yakni
            di Lembah Baliem, Nabire, Pegunungan Bintang, Jayawijaya, Yahukimo, dan lain-lain.</p>
    </div>
    <div class="container">
        <h3>4. Upacara Adat Aruh Baharin Suku Dayak</h3>
    </div>
    <div class="container">
        <p>Dalam buku PPKn Modul Tema 11 "Bersama Meskipun Beragam" Kemendikbud 2020, di lingkungan masyarakat Suku
            Dayak, Kalimantan juga terdapat tradisi upacara adat yang bernama Aruh Baharin.</p>
        <p>Upacara adat ini dilakukan sebagai bentuk ungkapan rasa syukur dan terima kasih kepada Tuhan Yang Maha Kuasa.
            Hal ini dikarenakan anugerah hasil panen yang diperoleh Suku Dayak melimpah, atau bisa juga keberhasilan
            dalam berdagang.</p>
        <p>Ritual ini sekaligus sebagai penghormatan kepada leluhur yang diyakini senantiasa melindungi mereka dari
            malapetaka.</p>
    </div>
    <div class="container">
        <h3>5. Upacara Potong Jari di Papua</h3>
    </div>
    <div class="container">
        <p>Mengutip dari Buku PPKn Indonesiaku Unik Kemendikbud 2018, Tradisi potong jari merupakan salah satu tradisi
            di Papua. Tradisi ini dilakukan ketika ada salah satu keluarga atau kerabat dekat seperti: suami, istri,
            ayah, ibu, atau anak meninggal dunia.</p>
        <p>Tradisi ini wajib dilakukan bagi masyarakat pegunungan tengah Papua. Mereka beranggapan bahwa memotong jari
            merupakan simbol dari sakit dan pedihnya seseorang yang kehilangan anggota keluarganya.</p>
        <p>Jari menjadi simbol kerukunan, kesatuan, dan kekuatan dalam melakukan suatu pekerjaan. Satu sama lain saling
            membantu dan melengkapi sehingga terjadi harmonisasi dalam hidup dan kehidupan. Jika salah satu hilang, maka
            hilanglah komponen kebersamaan dan berkuranglah kekuatan.</p>
    </div>
    <div class="container">
        <h3>6. Upacara Adat Tabuik</h3>
    </div>
    <div class="container">
        <p>Masih mengutip dari Buku PPKn Indonesiaku Unik Kemendikbud 2018, Upacara Tabuik merupakan sebuah tradisi
            masyarakat Minangkabau di pantai barat Sumatera Barat, tepatnya di pesisir Pantai Pariaman. Tradisi ini
            sudah diselenggarakan secara turun temurun. Upacara ini digelar pada hari Asura yang jatuh setiap tanggal 10
            Muharram dalam kalender islam.</p>
        <p>Kata Tabuik artinya peti pusaka peninggalan Nabi Musa yang digunakan dalam menyimpan naskah Perjanjian Bani
            Israel dengan Allah. Upacara ini juga memperingati meninggalnya cucu Nabi Muhammad SAW yang bernama Husein.
        </p>
        <p>Upacara Tabuik adalah perayaan melabuhkan tabuik ke laut, puncak acara Tabuik yang tinggi diarak ke tengah
            kota dengan diiringi tasa dan teriakan khas Hoyak Tabuik, lalu diputar, digoyang-goyang, dan perlahan-lahan
            dibawa ke pantai untuk dibuang ke laut pada senja hari.</p>
    </div>
    <div class="container">
        <h3>7. Upacara Adat Peusijuek di Aceh</h3>
    </div>
    <div class="container">
        <p>Upacara ini merupakan upacara sebagai ucapan rasa syukur terhadap rezeki yang diberikan Tuhan pada rakyat
            Aceh. Umumnya tradisi ini digunakan pada acara pernikahan, rumah baru, naik haji, hingga kelahiran.</p>
        <p>Tidak hanya itu, upacara ini juga bisa dilakukan saat seseorang mendapat kebahagiaan atau rahmat dan juga
            terlepas dari suatu musibah yang menimpa.</p>
    </div>

