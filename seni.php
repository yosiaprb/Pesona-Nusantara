    <div class="container">
        <h3>1. Tari Kecak, Bali</h3>
    </div>
    <div class="container">
        <p>Wisatawan yang pernah berkunjung ke Pulau Dewata tentunya sudah tak asing lagi dengan seni tari yang satu
            ini.</p>
        <p>Awalnya merupakan ritual kuno bernama Sanghyang yang dilakukan untuk mengusir roh jahat, kini Tari Kecak
            merupakan tarian khas Bali yang melengkapi destinasi wisata budaya dan rohani di sana.</p>
        <p>Selain pementasan tradisional, salah satu tari khas Bali ini bahkan juga kerap diadaptasi menjadi drama
            panggung yang tak kalah memukau, menceritakan pewayangan Ramayana yang terus berhasil menarik wisatawan
            lokal dan mancanegara.</p>
        <p>Salah satu keunikan tarian ini ialah suara “Cak! Cak! Cak!” dari 50-70 penarinya yang kemudian diiringi
            dengan keindahan suara gamelan.</p>
    </div>
    <div class="container">
        <h3>2. Tari Barong, Gianyar, Bali</h3>
    </div>
    <div class="container">
        <p>Masih dari Bali, seni pertunjukan tradisional lainnya yang tak kalah populer adalah Tari Barong yang berasal
            dari daerah Gianyar. Menariknya, tarian ini telah dikenal oleh wisatawan luar negeri sejak tahun 1930 silam!
        </p>
        <p>Kalau Tari Kecak ditampilkan oleh 50-70 penari, Tari Barong hanya menampilkan dua orang penari lincah dengan
            gerakan kompak yang indah.</p>
        <p>Dalam kepercayaan masyarakat Bali, Barong berwujud singa dalam tarian ini merepresentasikan Raja Roh yang
            merupakan sosok malaikat pelindung, sedangkan Rangda adalah Ratu Iblis dan merupakan lawan dari Barong.</p>
    </div>
    <div class="container">
        <h3>3. Pertunjukan Ramayana, Prambanan</h3>
    </div>
    <div class="container">
        <p>Jika berkunjung ke Daerah Istimewa Yogyakarta (DIY), Candi Prambanan merupakan destinasi wisata yang hampir
            tak pernah dilewatkan oleh wisatawan.</p>
        <p>Selain karena merupakan candi Hindu terbesar di Asia Tenggara, di sana pengunjung juga bisa menyaksikan seni
            pertunjukan tradisional bertajuk <i>“Sendratari Ramayana”</i> atau <i>“Ramayana Ballet”</i>.</p>
        <p>Pementasan berupa tarian dan drama yang dibawakan oleh lebih dari 200 penari serta diiringi oleh pemain
            gamelan tradisional itu ditampilkan tanpa dialog.</p>
        <p>Seni pertunjukan tradisional yang satu ini bahkan berhasil membuat Raja dan Ratu Belanda Willem-Alexander dan
            Maxima Zorreguieta Cerruti terpukau saat berkunjung pada Maret 2020 lalu.</p>
    </div>
    <div class="container">
        <h3>4. Pencak Silat</h3>
    </div>
    <div class="container">
        <p>Seni pertunjukan asal Indonesia yang telah mendunia lainnya adalah Pencak Silat. Ini merupakan seni bela diri
            yang dipercaya memiliki semua elemen yang membentuk warisan budaya Indonesia.</p>
        <p>Oleh sebab itu, tak heran apabila Pencak Silat masuk ke dalam daftar Warisan Budaya Takbenda UNESCO.
            Pencapaian ini menandakan bagaimana Pencak Silat berhasil memukau mata dunia.</p>
    </div>
    <div class="container">
        <h3>5. Angklung, Jawa Barat</h3>
    </div>
    <div class="container">
        <p>Alat musik tradisional Indonesia asal Jawa Barat, angklung, menjadi warisan budaya yang juga berhasil
            mendunia.</p>
        <p>Salah satu pertunjukan seni menggunakan alat musik tradisional ini yang banyak dikunjungi wisatawan dari
            berbagai daerah dan belahan dunia ialah Saung Mang Udjo.</p>
        <p>Sanggar seni tersebut berhasil menyebar keindahan alami alat musik yang terbuat dari bambu itu. Saking
            indahnya harmoni yang dikeluarkan oleh angklung, alat musik tradisional ini dinobatkan sebagai Warisan
            Budaya Takbenda asal Indonesia di UNESCO.</p>
    </div>
    <div class="container">
        <h3>6. Reog Ponorogo, Jawa Timur</h3>
    </div>
    <div class="container">
        <p>Selanjutnya ada Reog Ponorogo yang menjadi ikon Kota Ponorogo, Jawa Timur. Keunikan dari pertunjukan seni
            yang satu ini adalah kostum penampilnya yang mencapai 50 kilogram!</p>
        <p>Selain itu, kostum tersebut hanya diangkat menggunakan gigi para pemainnya. Inilah yang membuat Reog Ponorogo
            berhasil memukau berbagai wisatawan dari banyak negara.</p>
    </div>
    <div class="container">
        <h3>7. Gamelan, Jawa Tengah</h3>
    </div>
    <div class="container">
        <p>Tak hanya angklung, pementasan alat musik tradisional gamelan turut memukau dunia. Alat musik tradisional
            yang satu ini sudah menjadi bagian dari kehidupan masyarakat, terutama masyarakat Jawa yang menggunakannya
            untuk berbagai upacara adat hingga iringan tarian serta wayang.</p>
        <p>Keindahan gamelan bahkan membuat banyak masyarakat dari negara lain tertarik untuk mempelajarinya, seperti
            Australia, Kanada, Inggris, sampai Amerika Serikat.</p>
    </div>

